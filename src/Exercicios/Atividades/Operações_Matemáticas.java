package Exercicios.Atividades;

public class Operações_Matemáticas {
    static void main() {
        //criando as duas variáveis para receber os valores 10 e 5.
        int numero1 = 10;
        int numero2 = 5;

        //Logica e print
        System.out.printf("A soma entre %d + %d = %d", numero1, numero2, (numero1 + numero2));
        System.out.printf("\nA soma entre %d - %d = %d", numero1, numero2, (numero1 - numero2));
        System.out.printf("\nA soma entre %d * %d = %d", numero1, numero2, (numero1 * numero2));
        System.out.printf("\nA soma entre %d / %d = %d", numero1, numero2, (numero1 / numero2));

    }
}
