package Exercicios.OperadoresAritmeticos;

/*Considere duas variáveis inteiras a = 10 e b = 3.
Calcule a soma, subtração, multiplicação e divisão inteira, imprimindo cada resultado em uma linha.*/

public class Ex1 {
    static void main() {
        int a = 10;
        int b = 3;

        System.out.println("A soma é: " + (a + b));
        System.out.println("A subtração é: " + (a - b));
        System.out.println("A multplicação é: " + (a * b));
        System.out.println("A divisão é: " + (a / b));

    }
}
