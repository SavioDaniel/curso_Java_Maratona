package DevSkills.curso_Java_Maratona;

public class Aula5_OperadoresAritmeticosParte4 {
    static void main() {
        // =, +=, -=, *=, /=, %=

        double valor = 1500;
        valor += 1000; //2500
        valor -= 500; //2000
        valor *= 2; //4000
        valor /= 4; //1000
        valor %= 2; //0.0
        System.out.println(valor);
    }
}
